puts "Matthew Woodard" # prints Matthew Woodard on a new line
p "Matthew Woodard" # prints "Matthew Woodard" on a new line

puts 5            # prints 5 on a new line
puts "5"           # prints 5 on a new line

p 5              # prints 5 on a new line
p "5"            # prints "5" on a new line

p 5 + 5            # prints 10 on a new line
p "5 + 5"          # prints "5 + 5" on a new line

puts "Hi there\nline break"     # "Hi there" on one line, "line break" on the next
p "Hi there\nline break"    # "Hi there\nline break"